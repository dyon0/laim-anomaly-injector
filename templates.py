# ═══════════════════════════════════════════════════════════════════════
# Утилиты для работы с шаблонами
# ═══════════════════════════════════════════════════════════════════════

def render_template(template: str, **kwargs: str) -> str:
    """Безопасный рендер шаблона с подстановкой."""
    result = template
    for key, value in kwargs.items():
        result = result.replace('{' + key + '}', value)
    return result


def pick_random(items: List[str], rng: random.Random) -> str:
    """Выбрать случайный элемент из списка."""
    return rng.choice(items)


def truncate_text(text: str, max_len: int = 200) -> str:
    """Обрезать текст до max_len символов для вставки в шаблон."""
    if len(text) <= max_len:
        return text
    return text[:max_len] + '...'
